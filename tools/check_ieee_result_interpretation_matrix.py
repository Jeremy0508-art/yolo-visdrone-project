from __future__ import annotations

import re
from dataclasses import dataclass
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
MATRIX_PATH = ROOT / "paper/ieee_result_interpretation_matrix.md"
REPORT_PATH = ROOT / "paper/ieee_result_interpretation_matrix_audit.md"


@dataclass(frozen=True)
class Check:
    item: str
    status: str
    evidence: str
    action: str = ""


def status_label(status: str) -> str:
    return {"ready": "READY", "missing": "MISSING"}[status]


def contains(text: str, pattern: str) -> bool:
    return re.search(pattern, text, flags=re.IGNORECASE | re.MULTILINE) is not None


def audit() -> list[Check]:
    if not MATRIX_PATH.exists():
        return [
            Check(
                "IEEE result interpretation matrix exists",
                "missing",
                "paper/ieee_result_interpretation_matrix.md",
                "Create the matrix before moving result interpretation into the IEEE manuscript.",
            )
        ]

    text = MATRIX_PATH.read_text(encoding="utf-8", errors="ignore")
    checks: list[Check] = [
        Check("IEEE result interpretation matrix exists", "ready", "paper/ieee_result_interpretation_matrix.md")
    ]

    required_sources = [
        "main_comparison_for_paper.csv",
        "speed_results.csv",
        "model_complexity.csv",
        "ieee_scale_results_visdrone.csv",
        "ieee_scale_ap_results_visdrone.csv",
        "ieee_claim_boundary.md",
        "ieee_method_selection_protocol.md",
    ]
    for token in required_sources:
        checks.append(
            Check(
                f"Source evidence reference: {token}",
                "ready" if token in text else "missing",
                token if token in text else "not found",
                "" if token in text else "Add the evidence source to the matrix.",
            )
        )

    required_relations = [
        r"YOLO11n-640\s+vs\s+YOLO11n-960",
        r"YOLO11n-960\s+vs\s+YOLO11n-P2-960",
        r"YOLO11n-P2-960\s+vs\s+YOLO11n-P2-CA-960",
        r"YOLO11n-P2-960\s+vs\s+YOLOv8n-960",
        r"YOLO11n-P2-960\s+vs\s+YOLO11s-960",
        r"TOFC candidate",
        r"UAVDT cross-dataset route",
        r"VisDrone test-dev",
    ]
    for pattern in required_relations:
        checks.append(
            Check(
                f"Interpretation relation: {pattern}",
                "ready" if contains(text, pattern) else "missing",
                pattern if contains(text, pattern) else "not found",
                "" if contains(text, pattern) else "Add this interpretation branch.",
            )
        )

    required_boundaries = [
        ("No official AP-small shortcut", "official AP-small"),
        ("No state-of-the-art shortcut", "state-of-the-art"),
        ("No partial epoch shortcut", "partial epoch"),
        ("Abstract and conclusion updated last", "Abstract and Conclusion Rules"),
        ("Literature-only separation", "literature-only"),
    ]
    for item, token in required_boundaries:
        found = token in text
        checks.append(
            Check(
                f"Boundary rule: {item}",
                "ready" if found else "missing",
                token if found else "not found",
                "" if found else "Add this wording boundary.",
            )
        )

    safe_phrases = [
        ("TOFC small-object claim remains locked", "Do not claim TOFC improves small-object diagnostics over YOLO11n-P2-960"),
        ("Generalization remains locked", "Do not claim generalization beyond VisDrone"),
        ("Official test-dev remains locked", "No official returned test-dev metrics are available"),
    ]
    for item, phrase in safe_phrases:
        found = phrase in text
        checks.append(
            Check(
                f"Safe boundary present: {item}",
                "ready" if found else "missing",
                phrase if found else "not found",
                "" if found else "Add this explicit lock boundary to the matrix.",
            )
        )

    return checks


def write_report(checks: list[Check]) -> None:
    total = len(checks)
    ready = sum(1 for c in checks if c.status == "ready")
    missing = sum(1 for c in checks if c.status == "missing")
    lines = [
        "# IEEE Result Interpretation Matrix Audit",
        "",
        "This report is generated by `tools/check_ieee_result_interpretation_matrix.py`. It checks that the IEEE result interpretation matrix covers the main completed and pending evidence branches without endorsing unsupported claims.",
        "",
        "## Summary",
        "",
        f"- Total checks: {total}",
        f"- Ready: {ready}",
        f"- Missing: {missing}",
        "",
        "## Checks",
        "",
        "| Item | Status | Evidence | Action |",
        "| --- | --- | --- | --- |",
    ]
    for check in checks:
        lines.append(f"| {check.item} | {status_label(check.status)} | `{check.evidence}` | {check.action} |")
    lines.extend(
        [
            "",
            "## Interpretation",
            "",
            "- `READY` means the result-reading or boundary item is represented in the matrix.",
            "- `MISSING` means the matrix needs revision before it is used to guide IEEE manuscript prose.",
        ]
    )
    REPORT_PATH.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    checks = audit()
    write_report(checks)
    print(f"Wrote {REPORT_PATH.relative_to(ROOT)}")


if __name__ == "__main__":
    main()
